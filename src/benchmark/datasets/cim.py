"""CIM (Cross-domain Information Memory) dataset adapter."""

from __future__ import annotations

import hashlib
import json
import warnings
from collections import defaultdict
from pathlib import Path
from typing import Iterator

from benchmark.datasets import Sample
from benchmark.exceptions import FatalBenchmarkError


class CIMDataset:
    """Loads CIMemories dataset from HuggingFace and yields Samples.

    CIMemories schema (6 columns):
    - name (str): persona name
    - attribute (str): attribute ID
    - memory_statement (str): the memory text
    - label (float64): 1.0 = should be revealed, 0.0 = should NOT be revealed
    - prompt (str): user query/task
    - adv_reveal_prompt (str): adversarial variant (unused)

    Rows sharing the same (name, prompt) are grouped into a single Sample.

    When labels_file is provided, labels are loaded from a pre-computed JSON
    file (generated by cim_labeler.py) instead of the HuggingFace label column.
    """

    VALID_MEMORY_MODES = {"none", "relevant_only", "mixed", "full_profile"}

    def __init__(
        self,
        dataset_id: str = "facebook/CIMemories",
        memory_mode: str = "full_profile",
        split: str = "test",
        labels_file: str | Path | None = None,
    ) -> None:
        if memory_mode not in self.VALID_MEMORY_MODES:
            raise ValueError(
                f"Invalid memory_mode={memory_mode!r}. "
                f"Valid values: {sorted(self.VALID_MEMORY_MODES)}"
            )

        from datasets import load_dataset

        self._ds = load_dataset(dataset_id, split=split)
        self._memory_mode = memory_mode
        self._labels: dict[str, str | None] | None = None

        if labels_file is not None:
            from benchmark.datasets.cim_labeler import load_labels_file

            self._labels = load_labels_file(labels_file)
            labeled_count = sum(1 for v in self._labels.values() if v is not None)
            print(f"Loaded {labeled_count} labels from {labels_file}")

    def _get_label(self, name: str, prompt: str, attribute: str, hf_label: float | None) -> str | None:
        """Resolve label for an attribute.

        Priority: labels_file > HuggingFace label column.
        Returns 'share', 'private', or None.
        """
        if self._labels is not None:
            # Labels file uses 'name|prompt_hash|attribute' keys
            prompt_hash = hashlib.md5(prompt.encode()).hexdigest()[:12]
            label_key = f"{name}|{prompt_hash}|{attribute}"
            return self._labels.get(label_key)

        # Fall back to HuggingFace label column
        if hf_label == 1.0:
            return "share"
        elif hf_label == 0.0:
            return "private"
        return None

    def __iter__(self) -> Iterator[Sample]:
        # Group rows by (name, prompt)
        groups: dict[tuple[str, str], list[dict]] = defaultdict(list)
        for row in self._ds:
            key = (row["name"], row["prompt"])
            groups[key].append(row)

        # Fail fast if no labels source — judge cannot evaluate without them
        if self._labels is None:
            has_any_label = any(
                row["label"] is not None
                for rows in groups.values()
                for row in rows
            )
            if not has_any_label:
                raise FatalBenchmarkError(
                    "CIMemories dataset has no labels (all None in HuggingFace dataset). "
                    "The judge cannot evaluate without share/private attribute labels.\n\n"
                    "To fix this, generate labels first then pass them to the benchmark:\n"
                    "  1. uv run benchmark cim-label\n"
                    "  2. uv run benchmark run config_cim.json --cim-labels outputs/cim_labels.json\n\n"
                    "Or use --auto-label to do both in one step:\n"
                    "  uv run benchmark run config_cim.json --dataset cim --auto-label"
                )

        for (name, prompt), rows in groups.items():
            sample_id = hashlib.md5(
                json.dumps({"name": name, "prompt": prompt}, sort_keys=True).encode()
            ).hexdigest()

            required_attrs: list[str] = []
            forbidden_attrs: list[str] = []

            for r in rows:
                label = self._get_label(name, prompt, r["attribute"], r["label"])
                if label == "share":
                    required_attrs.append(r["attribute"])
                elif label == "private":
                    forbidden_attrs.append(r["attribute"])

            memories = self._select_memories(rows)

            # Full attribute->memory mapping (needed by REVEAL judge even when
            # memory_mode filters the memories the *model* sees).
            attribute_memory_map = {
                r["attribute"]: r["memory_statement"] for r in rows
            }

            yield Sample(
                sample_id=sample_id,
                prompt=prompt,
                memories=memories,
                required_attributes=required_attrs,
                forbidden_attributes=forbidden_attrs,
                metadata={
                    "failure_type": "cim",
                    "name": name,
                    "attribute_memory_map": attribute_memory_map,
                },
            )

    def _select_memories(self, rows: list[dict]) -> list[str]:
        """Select memory statements based on memory_mode."""
        if self._memory_mode == "none":
            return []
        elif self._memory_mode == "relevant_only":
            # When using labels file, resolve per-row
            result = []
            for r in rows:
                label = self._get_label(
                    r["name"], rows[0]["prompt"], r["attribute"], r["label"]
                )
                if label == "share":
                    result.append(r["memory_statement"])
            return result
        elif self._memory_mode == "mixed":
            relevant = []
            irrelevant = []
            for r in rows:
                label = self._get_label(
                    r["name"], rows[0]["prompt"], r["attribute"], r["label"]
                )
                if label == "share":
                    relevant.append(r["memory_statement"])
                elif label == "private":
                    irrelevant.append(r["memory_statement"])
            subset_size = min(len(irrelevant), len(relevant))
            return relevant + irrelevant[:subset_size]
        else:  # full_profile
            return [r["memory_statement"] for r in rows]
